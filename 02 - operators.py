#Variables
a = 10
b = 3

#Arithmetic Operators
print(a + b) #Addition
print(a - b) #Subtraction
print(a * b) #Multiplication
print(a / b) #Division

print(a ** b) # Exponentiation (10 to the power of 3)
print(a // b) # Floor division (divides and rounds down to nearest integer)
print(a % b)  # Modulus (gives the remainder of the division)

first_name = "John"
last_name = "Doe"

full_name = first_name + " " + last_name #Contatination/Appending
repeated_string = "Ha" * 3 #Generating String Given Number of Time

print(full_name)
print(repeated_string)

#Comparison Operators
print(a == b)  # Equal to
print(a != b)  # Not equal to
print(a > b)   # Greater than
print(a < b)   # Less than
print(a >= b)  # Greater than or equal to
print(a <= b)  # Less than or equal to


#Logical Operators
a = True
b = False
print(a and b)  # Logical AND (True only if both are True)
print(a or b)   # Logical OR (True if at least one is True)
print(not a)    # Logical NOT False (inverts the boolean value)